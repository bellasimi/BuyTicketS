package com.bts.mypage.vo;

public class ReviewVO {
	
	private String review_star;
	private String review_content;
	private String order_seq_num;
	
	public String getReview_star() {
		return review_star;
	}
	public void setReview_star(String review_star) {
		this.review_star = review_star;
	}
	public String getReview_content() {
		return review_content;
	}
	public void setReview_content(String review_content) {
		this.review_content = review_content;
	}
	public String getOrder_seq_num() {
		return order_seq_num;
	}
	public void setOrder_seq_num(String order_seq_num) {
		this.order_seq_num = order_seq_num;
	}

	
}
